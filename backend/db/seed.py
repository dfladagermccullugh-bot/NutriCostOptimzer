def seed():
    os.makedirs(os.path.dirname(DB_PATH), exist_ok=True)
    conn = sqlite3.connect(DB_PATH)
    conn.execute("""
        CREATE TABLE IF NOT EXISTS foods (
            fdc_id INTEGER PRIMARY KEY,
            description TEXT NOT NULL,
            category TEXT,
            calories_per_100g REAL NOT NULL,
            protein_per_100g REAL NOT NULL,
            carbs_per_100g REAL NOT NULL,
            fat_per_100g REAL NOT NULL,
            source TEXT DEFAULT 'local',
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
        )
    """)
    conn.execute("CREATE INDEX IF NOT EXISTS idx_foods_description ON foods(description)")
    conn.executemany(
        "INSERT OR IGNORE INTO foods (fdc_id, description, category, calories_per_100g, protein_per_100g, carbs_per_100g, fat_per_100g) VALUES (?, ?, ?, ?, ?, ?, ?)",
        FOODS,
    )
    conn.commit()
    count = conn.execute("SELECT COUNT(*) FROM foods").fetchone()[0]
    conn.close()
    print(f"Seeded {count} foods into {DB_PATH}")


if __name__ == "__main__":
    seed()
